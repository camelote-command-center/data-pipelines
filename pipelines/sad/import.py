#!/usr/bin/env python3
"""
SAD (Service des autorisations de construire) -- Incremental Import Pipeline

Fetches Geneva construction permit dossiers from the SAD public API and
upserts into bronze."SAD" on lamap_db.

INCREMENTAL STRATEGY:
    - Sort by dateDernierDepot DESC (most recently modified first)
    - Page through results, stop when all records on a page are older than
      the cutoff date (last_acquired_at from command center or 30 days ago)
    - UPSERT all fetched records (full overwrite of all fields)

The SAD API requires:
    1. GET the search page to obtain a session + XSRF token
    2. POST search requests with X-XSRF-TOKEN header
    3. Commune filter uses commune NAME strings (not OFS codes)
    4. Type filter is required

DATA SAFETY:
    - UPSERT only (INSERT ... ON CONFLICT DO UPDATE).
    - Never truncates or deletes existing data.
    - Full field overwrite on update (SAD dossiers evolve over time).

Environment variables:
    RE_LLM_SUPABASE_URL                 - re-LLM Supabase project URL (required)
    RE_LLM_SUPABASE_SERVICE_ROLE_KEY    - re-LLM service_role key     (required)
    RE_LLM_SCHEMA                       - target schema               (default: bronze_ch)
    CAMELOTE_SUPABASE_URL               - Camelote command-center URL (optional)
    CAMELOTE_SUPABASE_KEY               - Camelote service_role key   (optional)

re-LLM is the sole write target. Production data flows lamap_db ← FDW ←
re-LLM via gold_ch.run_sync_proc('daily').
"""

