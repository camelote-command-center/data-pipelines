# Main
# ------------------------------------------------------------------

def main():
    rellm_url = os.environ.get("RE_LLM_SUPABASE_URL", "")
    rellm_key = os.environ.get("RE_LLM_SUPABASE_SERVICE_ROLE_KEY", "")
    rellm_schema = os.environ.get("RE_LLM_SCHEMA", "bronze_ch")
    camelote_url = os.environ.get("CAMELOTE_SUPABASE_URL", "")
    camelote_key = os.environ.get("CAMELOTE_SUPABASE_KEY", "")

    if not rellm_url or not rellm_key:
        print("ERROR: RE_LLM_SUPABASE_URL and RE_LLM_SUPABASE_SERVICE_ROLE_KEY are required")
        sys.exit(1)

    print("=" * 60)
    print("  SAD (Construction Permits) Pipeline — Incremental")
    print(f"  Target: {rellm_schema}.{TABLE}")
    print("=" * 60)

    # -- Determine cutoff date --
    meta = get_dataset_meta(camelote_url, camelote_key, DATASET_CODE)
    if meta and meta.get("last_acquired_at"):
        cutoff_date = meta["last_acquired_at"].replace(tzinfo=None)
        print(f"\n  Last acquired: {cutoff_date.isoformat()}")
        print(f"  Previous record count: {meta.get('record_count', 'unknown')}")
    else:
        cutoff_date = datetime.now() - timedelta(days=DEFAULT_LOOKBACK_DAYS)
        print(f"\n  No previous metadata found, using {DEFAULT_LOOKBACK_DAYS}-day lookback")

    print(f"  Cutoff: {cutoff_date.strftime('%Y-%m-%d')}")

    # -- Discover table columns --
    print("\n  Discovering table columns...")
    table_columns = get_table_columns(rellm_url, rellm_key, rellm_schema, TABLE)
    if table_columns:
        allowed = table_columns - EXCLUDE_COLUMNS
        print(f"  Table has {len(table_columns)} columns, {len(allowed)} writable")
    else:
        print("  Warning: Could not discover columns, will send all fields")
        allowed = None

    # -- Row count BEFORE --
    rows_before = get_row_count(rellm_url, rellm_key, rellm_schema, TABLE)
    print(
        f"  Rows before: {rows_before:,}" if rows_before is not None
        else "  Rows before: unknown"
    )

    # -- Create SAD session --
    print("\n  Connecting to SAD API...")
    session, xsrf = create_sad_session()

    # -- Get commune list --
    communes = get_communes(session, xsrf)
    print(f"  Communes: {len(communes)}")

    # -- Get total count --
    total_in_api = get_total_count(session, xsrf)
    print(f"  Total dossiers in SAD: {total_in_api:,}")

    # -- Incremental fetch --
    start_time = time.time()
    print(f"\n{'━' * 60}")
    print("  Fetching modified dossiers...")
    print(f"{'━' * 60}")

    all_records = fetch_incremental(session, xsrf, cutoff_date, communes)

    fetch_elapsed = time.time() - start_time
    print(f"\n  Fetch complete: {len(all_records):,} records in {fetch_elapsed:.0f}s")

    if not all_records:
        print("  No modified dossiers found since cutoff date.")
        # Update metadata anyway to mark successful check
        update_dataset_meta(
            camelote_url, camelote_key, DATASET_CODE,
            record_count=rows_before,
            status="active",
        )
        print("=" * 60)
        return

    # -- Filter to known columns --
    if allowed:
        filtered_records = []
        dropped_cols = set()
        for rec in all_records:
            filtered = {}
            for k, v in rec.items():
                if k in allowed:
                    filtered[k] = v
                else:
                    dropped_cols.add(k)
            filtered_records.append(filtered)
        if dropped_cols:
            print(f"  Dropped unknown columns: {', '.join(sorted(dropped_cols))}")
        all_records = filtered_records

    # -- Normalise keys (all records must have same keys for PostgREST) --
    all_keys = set()
    for r in all_records:
        all_keys |= r.keys()
    for r in all_records:
        for k in all_keys:
            r.setdefault(k, None)

    # -- Upsert --
    print(f"\n  Upserting {len(all_records):,} records (batch size {BATCH_SIZE})...")

    total_upserted = batch_upsert(
        url=rellm_url,
        key=rellm_key,
        table=TABLE,
        records=all_records,
        conflict_column=CONFLICT_COLUMN,
        schema=rellm_schema,
        batch_size=BATCH_SIZE,
    )

    # -- Row count AFTER --
    rows_after = get_row_count(rellm_url, rellm_key, rellm_schema, TABLE)

    # -- Summary --
    elapsed = time.time() - start_time
    net_new = 0
    if rows_before is not None and rows_after is not None:
        net_new = rows_after - rows_before

    print(f"\n{'=' * 60}")
    print("  IMPORT COMPLETE")
    print(f"  Records fetched:  {len(all_records):,}")
    print(f"  Rows upserted:    {total_upserted:,}")
    print(f"  Net new:          {net_new:,}")
    print(f"  Rows before:      {rows_before:,}" if rows_before is not None else "  Rows before:      unknown")
    print(f"  Rows after:       {rows_after:,}" if rows_after is not None else "  Rows after:       unknown")
    print(f"  Duration:         {elapsed / 60:.1f} min")
    print("=" * 60)

    if total_upserted == 0:
        print("  FAILED: Zero rows upserted!")
        sys.exit(1)

    # -- Update dataset metadata --
    update_dataset_meta(
        camelote_url, camelote_key, DATASET_CODE,
        record_count=rows_after,
        status="active",
    )


if __name__ == "__main__":
    main()
