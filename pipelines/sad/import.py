def get_total_count(
    session: requests.Session, xsrf: str, commune: str = ""
) -> int:
    """Get total record count from SAD API."""
    body = {
        "type": SAD_TYPES,
        "page": 1,
        "lignesParPage": 15,
        "sortBy": "dateDernierDepot",
        "sortDesc": True,
    }
    if commune:
        body["commune"] = commune

    try:
        r = session.post(
            SAD_COUNT_URL, json=body, headers=sad_headers(xsrf), timeout=30
        )
        if r.status_code == 200:
            return int(r.text.strip())
    except Exception:
        pass
    return 0


def fetch_page(
    session: requests.Session,
    xsrf: str,
    commune: str,
    page: int,
    page_size: int = PAGE_SIZE,
) -> list[dict]:
    """Fetch one page of SAD dossiers for a commune."""
    body = {
        "type": SAD_TYPES,
        "commune": commune,
        "page": page,
        "lignesParPage": page_size,
        "sortBy": "dateDernierDepot",
        "sortDesc": True,
    }
    for attempt in range(1, 4):
        try:
            r = session.post(
                SAD_SEARCH_URL, json=body, headers=sad_headers(xsrf), timeout=60
            )
            if r.status_code == 200:
                data = r.json()
                if isinstance(data, list):
                    return data
                return []
            elif r.status_code >= 500:
                wait = 5 * attempt
                print(f"    Server error {r.status_code}, retrying in {wait}s...")
                time.sleep(wait)
            else:
                print(f"    API error {r.status_code}: {r.text[:200]}")
                return []
        except Exception as e:
            wait = 5 * attempt
            print(f"    Request error ({e}), retrying in {wait}s...")
            time.sleep(wait)

    return []


def fetch_decisions(
    session: requests.Session, xsrf: str, business_key: str
) -> list[dict] | None:
    """Fetch decisions for a dossier."""
    url = f"{SAD_DECISIONS_URL}?businessKey={business_key}"
    try:
        r = session.get(url, headers=sad_headers(xsrf), timeout=15)
        if r.status_code == 200:
            data = r.json()
            if isinstance(data, list):
                return data
    except Exception:
        pass
    return None


def transform_record(raw: dict) -> dict:
    """
    Transform a SAD API record to match the bronze table schema.
    Uses explicit field mappings where available, falls back to camelCase→snake_case.
    """
    record = {}
    for k, v in raw.items():
        snake_key = FIELD_OVERRIDES.get(k, camel_to_snake(k))
        # Normalise empty strings to None
        if isinstance(v, str) and not v.strip():
            v = None
        record[snake_key] = v

    # Build the SAD URL
    dtype = record.get("type")
    numero = record.get("numero")
    complement = record.get("numero_complementaire")
    if dtype and numero:
        record["url"] = (
            f"https://app2.ge.ch/sadconsult/dossier/"
            f"{dtype}/{numero}/{complement or '1'}"
        )

    return record


# ------------------------------------------------------------------
# Incremental fetch logic
# ------------------------------------------------------------------

def fetch_incremental(
    session: requests.Session,
    xsrf: str,
    cutoff_date: datetime,
    communes: list[str],
) -> list[dict]:
    """
    Fetch all SAD dossiers modified since cutoff_date.

    Strategy: For each commune, page through results sorted by
    dateDernierDepot DESC. Stop when we hit records whose latest
    date fields (dateStatut, dateDepot) are ALL older than cutoff.
    """
    all_records = []
    cutoff_str = cutoff_date.strftime("%Y-%m-%d")

    print(f"\n  Cutoff date: {cutoff_str}")
    print(f"  Communes: {len(communes)}")

    for ci, commune in enumerate(communes):
        page = 1
        commune_records = 0
        stop_commune = False

        while not stop_commune:
            items = fetch_page(session, xsrf, commune, page)

            if not items:
                break

            page_has_recent = False

            for item in items:
                # Check if this record is recent enough
                # Use dateStatut as the best proxy for "last modified"
                date_statut = parse_sad_date(item.get("dateStatut"))
                date_depot = parse_sad_date(item.get("dateDepot"))
                date_acceptation = parse_sad_date(item.get("dateAcceptation"))

                # Take the most recent date as "last modified"
                dates = [d for d in [date_statut, date_depot, date_acceptation] if d]
                most_recent = max(dates) if dates else None

                if most_recent and most_recent >= cutoff_date:
                    page_has_recent = True

                # Transform and collect record regardless of date
                # (the sort is approximate, so we collect everything
                # on pages that have at least one recent record)
                record = transform_record(item)
                all_records.append(record)
                commune_records += 1

            # If NO records on this page were recent, stop this commune
            if not page_has_recent:
                break

            # If we got fewer results than page size, we've exhausted this commune
            if len(items) < PAGE_SIZE:
                break

            page += 1

            # Small delay between pages
            time.sleep(0.5)

        if commune_records > 0:
            print(f"  [{ci+1}/{len(communes)}] {commune}: {commune_records} records ({page} pages)")

        # Small delay between communes
        time.sleep(0.3)

    return all_records


