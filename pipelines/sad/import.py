def create_sad_session() -> tuple[requests.Session, str]:
    """
    Create a SAD API session by visiting the search page.
    Returns (session, xsrf_token).
    """
    s = requests.Session()
    r = s.get(SAD_INIT_URL, timeout=30)
    r.raise_for_status()

    xsrf = s.cookies.get("XSRF-TOKEN")
    if not xsrf:
        raise RuntimeError("Could not obtain XSRF token from SAD")

    print(f"  SAD session created (XSRF: {xsrf[:12]}...)")
    return s, xsrf


def sad_headers(xsrf: str) -> dict:
    """Build headers for SAD API requests."""
    return {
        "Content-Type": "application/json",
        "X-XSRF-TOKEN": xsrf,
    }


